package gavaconnectsdks.com.github.model;

public class Obligation {
    private String obligationId;
    private String obligationName;
    private String obligationType;

    public void setObligationId(String obligationId) {
        this.obligationId = obligationId;
    }

    public void setObligationName(String obligationName) {
        this.obligationName = obligationName;
    }

    public void setObligationType(String obligationType) {
        this.obligationType = obligationType;
    }

    public String getObligationId() {
        return this.obligationId;
    }

    public String getObligationName() {
        return this.obligationName;
    }

    public String getObligationType() {
        return this.obligationType;
    }

    
}
